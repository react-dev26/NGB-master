package com.epam.ngb.cli.entity;

/**
 * {@code {@link BaseEntity}} provides a minimum entity representation in the code
 */
public class BaseEntity {
    /**
     * {code id} is an ID used to identify an entity within it's type, so it can be the same for
     * the different {@code BiologicalDataItem} entities with different format (BAM and VCf files,
     * for instance
     */
    private Long id;

    /**
     * Name of the instance, must be unique since we allow finding items by name
     */
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
